TH1* hist_capture(TVirtualPad* fPad){
	if(fPad){
		TObjLink *lnk = fPad->GetListOfPrimitives()->FirstLink();
		while (lnk) {
			if(lnk->GetObject()->InheritsFrom("TH1"))return (TH1*)lnk->GetObject();
			lnk = lnk->Next();
		}
	}
	return 0;
}

